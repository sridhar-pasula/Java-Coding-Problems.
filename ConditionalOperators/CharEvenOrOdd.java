package ConditionalOperators;

public class CharEvenOrOdd {
    public static void main(String[] args) {
        
    
    char ch = 'a';
if(ch%2==0){
    System.out.println("Ascii Value of "  + ch + " is an even number");
}else{
    System.out.println("Ascii Value of "  + ch+ " is odd number");
}
}
}