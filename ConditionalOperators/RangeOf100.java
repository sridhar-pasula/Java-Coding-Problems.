package ConditionalOperators;

public class RangeOf100 {
    public static void main(String[] args) {
        int num=110,num1=100;
        if(num>=num1-9&&num<=num1+9)
        {
        System.out.println(num+" is in 10 range of 100");
        }else
        System.out.println(num+" not in 10 range of 100");
    }
}
